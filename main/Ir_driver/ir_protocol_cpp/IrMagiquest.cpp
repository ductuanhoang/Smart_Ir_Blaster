void Ir_Magiquest::sendMagiQuest(const uint64_t data, const uint16_t nbits, const uint16_t repeat)
{
	_ir_magiquest.sendGeneric(0, 0,  // No Headers - Technically it's included in the data.
	                     // i.e. 8 zeros.
	              kMagiQuestMarkOne, kMagiQuestSpaceOne, kMagiQuestMarkZero,
	              kMagiQuestSpaceZero,
	              0,  // No footer mark.
	              kMagiQuestGap, data, nbits, 36, true, repeat, 50);
}
